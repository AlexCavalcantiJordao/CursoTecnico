package TiposDeOperadores;

public class OperadoresDeComparacao {
 
    public static void main(String[] args) {
       
        boolean DezMaiorDoQueVinte = 10 >= 20;
        boolean DezMenorDoQueVinte = 10 <= 20;
        System.out.println(DezMaiorDoQueVinte);
        System.out.println(DezMenorDoQueVinte);
        System.out.println(5 == 5);
    }
}